import UIKit

extension UIColor {
    
    static var ckBlue: UIColor {
        return UIColor(red: 21.0/255.0, green: 31.0/255.0, blue: 44.0/255.0, alpha: 1)
    }
    
    static var ckGray: UIColor {
        return UIColor(red: 0.95, green: 0.95, blue: 0.95, alpha: 1)
    }
    
}
